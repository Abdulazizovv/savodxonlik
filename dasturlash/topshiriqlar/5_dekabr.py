# utils.py faylida quyidagi funksiyalarni yarating:
# 1. add(a, b) - ikki sonni qabul qilib, ularning yig'indisini qaytaradi.
# 2. subtract(a, b) - ikki sonni qabul qilib, ularning ayirmasini qaytaradi.
# 3. multiply(a, b) - ikki sonni qabul qilib, ularning ko'paytmasini qaytaradi.
# 4. divide(a, b) - ikki sonni qabul qilib, birinchisini ikkinchisiga bo'lib, natijani qaytaradi. 
# Agar ikkinchi son nolga teng bo'lsa, "Nolga bo'lish mumkin emas" xabarini qaytarsin.
# 5. power(a, b) - ikki sonni qabul qilib, birinchisini ikkinchisiga darajaga ko'tarib qaytaradi.

# module1.py faylida quyidagi funksiyalarni yarating:
# 1. factorial(n) - bitta butun sonni qabul qilib, uning faktorialini qaytaradi.
# 2. fibonacci(n) - bitta butun sonni qabul qilib, n-ta Fibonacci sonini qaytaradi.
# 3. to_uppercase(s) - bitta matn qabul qilib, uni katta harflarga aylantirib qaytaradi.

# main.py faylida quyidagi ishlarni bajaring:
# 1. utils.py va module1.py fayllaridan barcha funksiyalarni import qiling.
# 2. Foydalanuvchidan ikkita son va bitta matn qabul qiling.
# 3. Quyidagi amallarni bajarib, natijalarni konsolga chiqaring:
#    - Ikkita sonni qo'shing, ayiring, ko'paytiring, bo'ling va darajaga ko'taring.
#    - Foydalanuvchidan bitta butun son qabul qilib, uning faktorialini va Fibonacci sonini hisoblang.
#    - Foydalanuvchidan qabul qilingan matnni katta harflarga aylantiring va natijani chiqaring.
# 4. Har bir natijani tushuntiruvchi matn bilan birga konsolga chiqaring.

# Eslatma: Har bir faylni alohida-alohida yaratib, kerakli funksiyalarni yozing va main.py faylida ularni chaqiring.

